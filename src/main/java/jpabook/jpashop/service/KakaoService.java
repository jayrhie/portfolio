    public Member distinguishKakaoId(Long kakaoId) {

        if (!memberRepository.findByKakaoId(kakaoId).isPresent()) {
            return null;
        }
        Member kakaoMember = memberRepository.findByKakaoId(kakaoId).get();

        return kakaoMember;
    }
